<h2 class="h2">Wie funktioniert unsere Plattform?</h2>
<p>
	Du verstehst nicht, wie das hier alles funktioniert? Kein Problem! Auf dieser Seite wird dir alles
	erklärt, was du wissen musst.
</p>
<h2>Wie funktioniert unsere Webseite?</h2>
<p>
	Um auf der Webseite zu suchen, wähle zuerst den Lernmaterialien Tab aus. In diesem wähle ein Fach,
	wie Mathe, aus und gib dann einen Suchbegriff ein. Du kannst die Ergebnisse nach Kriterien wie
	Klasse, Titel, Schule, Jahr oder Beschreibung filtern. Starte die Suche und durchstöbere die
	Ergebnisse, um das passende Lernmaterial zu finden.
</p>
<h2>Wie funktioniert der Discord-Server?</h2>
<p>
	Wenn du neu auf dem Server bist, musst du zuerst die Regeln akzeptieren. Dies kannst du tun, indem
	du im Kanal "Regeln" auf "akzeptieren" drückst. Dann hast du die Möglichkeit, in den verschiedenen
	Chats mit den anderen zu kommunizieren. Dort gibt es dann auch den Bereich "Nachhilfe". Dort
	kannst du jederzeit einen neuen Post im Forum erstellen. Dort gibt es dann auch noch einen
	Anleitungspost, falls du nicht weiter kommst.
</p>
